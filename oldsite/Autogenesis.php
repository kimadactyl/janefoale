<h2>Autogenesis</h2>

<p>This series of works was inspired by research in the Caldbeck Fells in Cumbria, an area renowned for the scale and diversity of its mineral wealth. It explores analogies between the way minerals behave to create crystals and other forms, with the way the various physical qualities of pigments interact to create images with only the most minimal of human intervention.</p>

<p>The works are mixed media on paper.</p>
