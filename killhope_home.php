<h1>From Time to Time</h1>

<p><strong>An Artist&rsquo;s Residency at:  Killhope - the North of England Lead Mining Museum, May 2008 - May 2009</strong></p>

<blockquote>
  &ldquo;I&rsquo;m driving up the Al - in limbo between two locations - home in suburban West Yorkshire and what might become almost another home in Upper Weardale - a remote valley high in the North Pennines of County Durham. It is a transition from familiar comforts and reassurances to the uncertainty and confusion of a new role in unfamiliar surroundings.  It seems a significant step between two very different places.  I feel excitement, exhilaration and apprehension in roughly equal measure.&rdquo;<br/>
  <em>JF, Diary entry, 18 February 2008.</em>
</blockquote>

<blockquote>
  &ldquo;Hail, then snow, then both together as, confronting a bitingly cold north wind, I make my way up a moorland track between two disused fluorite mines.  I wonder how and where to start.  In my rucksack are packed sketchbook, paints, pens and pencils, geological hammer, map, camera, sound recorder, plastic bags to collect things in, a small plastic trowel and, presumably, somewhere, some sealing wax and an indeterminate length of string.  The scale of this miscellany reflects a frenzied, if somewhat unfocused, enthusiasm.&rdquo;<br/>  
  <em>JF, Diary entry, 19 February 2008.</em>
</blockquote>

