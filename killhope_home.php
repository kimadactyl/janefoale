<p>The <a href="http://www.killhope.org.uk">Residency at Killhope</a> involved the artist being in Weardale for about three days roughly every fortnight.  <a href="http://www.northpennines.org.uk">Staying at Cowshill, a couple of miles down the valley</a>, she spent time both around the Museum, where she had a portakabin to work in, and in the surrounding landscape.</p>

<p>The main components of the Residency were:</p>

<ul>
  <li>producing work at Killhope both in the Portakabin and in the grounds.</li>
  <li>exploring the historic mining landscape.</li>
  <li>delivering interpretive and outreach work at the Museum and in local schools.</li>
</ul>

<p>Jane&lsquo;s work on this project has inspired <a href="http://www.northpennines.org.uk/index.cfm?articleid=14384">&lsquo;Framing Time&rsquo;, an AV piece made by photographer Alex Sawyer and sound artist Phil Ogg</a> with Steven Kelly, during one of the artistâ€™s workshops at Killhope Lead Mining Museum</p>

