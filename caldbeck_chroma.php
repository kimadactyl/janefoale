<h1>Caldbeck Chroma</h1>

<p>Five years ago a series of coincidences led Jane Foale to the Caldbeck Fells, one of the quieter areas of the Lake District National Park, whose scenic landscape, unique geology and archaeological sites have led them to be both a Site of Special Scientific Interest and a Special Area of Conservation.  During 2009 the artist worked towards an exhibition of work inspired by their minerals.</p>

<p>Whilst researching in the area Foale has cooperated closely with the Lake District National Park Authority who control mineral collecting through a permit system.  &ldquo;Jane Foale&rsquo;s application for a permit was an unusual one for us,&rdquo; said Graham Standring, National Park Ranger. &ldquo;We saw it as an excellent opportunity to explore alternative approaches to interpreting this unique area.  Jane collected only small surface samples which provided sufficient material for her research. The resulting art work is both thought-provoking and very beautiful&rdquo;.</p>

<p>&ldquo;I have been fascinated by geology all my life but in the last few years the Caldbeck Fells have captivated me,&rdquo; comments the artist.  &lsquo;The colours and structure of the minerals and the processes by which they form are really absorbing.  My work develops by visiting public and private collections and spending time out on the Fells in all weathers.</p>

<p>&ldquo;The colours of the minerals echo those in this gently undulating yet wild upland area and this and the sense of process are things which I try to reveal in my work.  Using the different physical and chemical qualities of traditional and contemporary pigments, I allow the materials to create their own image through their interaction in an analogous way to that in which minerals develop, a balance between their innate composition and the context in which they find themselves.&rdquo;</p>